package bash.socialbuddies.beans;

public class BeanUsuario {
    private String usu_nombre,usu_apellido,usu_edad, usu_correo,usu_perfil;

    public BeanUsuario() {
    }

    public String getUsu_nombre() {
        return usu_nombre;
    }

    public String getUsu_apellido() {
        return usu_apellido;
    }

    public String getUsu_edad() {
        return usu_edad;
    }

    public String getUsu_correo() {
        return usu_correo;
    }

    public String getUsu_perfil() {
        return usu_perfil;
    }

    public void setUsu_nombre(String usu_nombre) {
        this.usu_nombre = usu_nombre;
    }

    public void setUsu_apellido(String usu_apellido) {
        this.usu_apellido = usu_apellido;
    }

    public void setUsu_edad(String usu_edad) {
        this.usu_edad = usu_edad;
    }

    public void setUsu_correo(String usu_correo) {
        this.usu_correo = usu_correo;
    }

    public void setUsu_perfil(String usu_perfil) {
        this.usu_perfil = usu_perfil;
    }
}
